def fill_weekly_schedule(data):
    conn = create_connection()
    cursor = conn.cursor()
    for entry in data:
        cursor.execute("""
        INSERT INTO weekly_schedule (week_type, day, pair_number, start_time, subject, teacher, place)
        VALUES (?, ?, ?, ?, ?, ?, ?)
        """, entry)
    conn.commit()
    conn.close()
    print(f"{len(data)} записей добавлено.")

