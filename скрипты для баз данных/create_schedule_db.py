import sqlite3

# Путь к базе
DB_PATH = "schedule.db"

# Создаём подключение
def create_connection():
    return sqlite3.connect(DB_PATH)

# Создание таблицы weekly_schedule
def create_weekly_schedule_table():
    conn = create_connection()
    cursor = conn.cursor()
    cursor.execute("""
    CREATE TABLE IF NOT EXISTS weekly_schedule (
        id INTEGER PRIMARY KEY AUTOINCREMENT,
        week_type TEXT NOT NULL,     -- "even" или "odd"
        day TEXT NOT NULL,           -- Понедельник, Вторник и т.д.
        pair_number INTEGER NOT NULL,
        start_time TEXT NOT NULL,
        subject TEXT,
        teacher TEXT,
        place TEXT
    )
    """)
    conn.commit()
    conn.close()
    print("Таблица weekly_schedule создана или уже существует.")

